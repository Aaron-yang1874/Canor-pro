export function assembleSystemPrompt(modules: string[] = []): string {
  const parts = [GLOBAL_SYSTEM_PROMPT, SAFETY_SYSTEM_PROMPT];
  if (modules.includes("evolution") || modules.includes("all")) {
    parts.push(ENGINE_CORE_PROMPT);
  }
  if (modules.includes("federated") || modules.includes("all")) {
    parts.push(FEDERATED_MODULE_PROMPT);
  }
  if (modules.includes("ui") || modules.includes("all")) {
    parts.push(UI_INTERACTION_PROMPT);
  }
  parts.push(ERROR_HANDLING_PROMPT);
  return parts.join("\n\n");
}
